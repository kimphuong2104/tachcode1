"""
This module is responsible for generating documentation content to be used by
the documentation portal. The documentation content is taken from *.[f]pickle
files generated by sphinx.

There are two types of content generated:
  1. Documentation html fragments and navigation information inside a sqlite db
  2. Sequential search index of the textual content of the docsets

Each of these content types are generated once for each package.
"""

