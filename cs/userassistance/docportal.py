_logger = logging.getLogger(__name__)

PORTAL_DB_NAME = 'docportal.db'
INDEX_DIR_NAME = '_docindex'
TEASER_PAGE = 'teaser'
MASTER_DOC = 'index'


def main():
    """
    Entry point for running as a script
    """
    params = sys.argv
    if len(params) < 5:
        print('Usage: docindex doc_dir build_dir html,pdf docsetame')
        return 1

    doc_dir = params[1]
    build_dir = params[2]
    builders = params[3].split(',')
    docsetname = params[4]
    return parse_docset(doc_dir, build_dir, builders, docsetname)


def parse_docset(doc_dir, build_dir, builders, docsetname):
    """
    This function builds and updates files for the documentation portal from
    the result of sphinx pickle builder. It will initially create and then
    update the package-wide docportal.db and corresponding docindex folder
    (both usually found in /doc). `docportal.db` contains the content of all
    docsets (excluding binaries like pdf and images), which will be read
    by the docportal to show the documentation for the package. `_docindex`
    contains files required by the search engine of the documentation portal.

    :param doc_dir: Root path of doc portal files
    :param build_dir: Path to the docset containing html and pickle folders
    :param builders: List of the builder formats to determine which formats
           should be offered for download (e.g. pdf)
    :param docsetname: Name of the docset. Used to find the additional
           format binaries.
    """
    portaldb_path = os.path.abspath(os.path.join(doc_dir, PORTAL_DB_NAME))

    portal_db = DocPortalDB(portaldb_path)

    docset = PickleDocset(build_dir, doc_dir)
    docset.scan()
    docset.scan_formats(builders, docsetname)
    docset.write(portal_db.db)


def clean_docsets(doc_dir, exclude=None):
    """
    If parameter `exclude` is provided, then remove all docsets and
    corresponding entries in the search index which are not listed in the
    parameter `exclude`. Otherwise, remove the search index and docportal db
    entirely.

    :param doc_dir: The path to doc directory
    :param exclude: List of docsets to keep (e.g. [`doc/admin/de`])
    """
    portaldb_path = os.path.abspath(os.path.join(doc_dir, PORTAL_DB_NAME))
    if exclude:
        portal_db = DocPortalDB(portaldb_path)
        docsets = portal_db.all_docsets()
        exclude_keys = []
        for ds_handle in exclude:
            keys = ds_handle.split('/', 2)
            # read category of the docset. When the category of a book changes,
            # it will be created anew with a new category relationship.
            # In order to remove the old book with the old category
            # relationship, we have to compare categories as well.
            pickle_path = os.path.join(doc_dir, keys[1], keys[2], 'pickle')
            docset_info = PickleDocset.read_docset_info(pickle_path)
            # In case docset.info could not be found, the docset will be
            # cleared. A valid docset should have a docset.info, otherwise
            # it's category cannot be determined.
            if docset_info:
                cat_title = docset_info.get('category')
                exclude_keys.append((keys[1], keys[2], cat_title))
        docsets_to_delete = (
            ds
            for ds in docsets
            if (ds.relpath, ds.lang, ds.cat_title) not in exclude_keys
        )

        for ds in docsets_to_delete:
            ds.Delete(portal_db.db, clean_categories=True)
            PickleDocset.clear_book_index(doc_dir, ds.name, ds.cat_title, ds.lang)

    else:
        index_path = os.path.abspath(os.path.join(doc_dir, INDEX_DIR_NAME))
        if os.path.exists(index_path):
            shutil.rmtree(index_path)
        if os.path.exists(portaldb_path):
            os.remove(portaldb_path)


class FakeConfig:
    """Fake sphinx config to be used to read the sphinx build environment."""

    def __init__(self, values):
        self.values = values

    def __getattr__(self, name):
        if name not in self.values:
            raise AttributeError(f'No such config value: {name}')
        return self.values[name]

    def __getitem__(self, name):
        return getattr(self, name)

    def __setitem__(self, name, value):
        setattr(self, name, value)

    def __delitem__(self, name):
        delattr(self, name)

    def __contains__(self, name):
        return name in self.values


